//! `ServerHandler` implementation backing the MCP server.
//!
//! [`Handler`] is instantiated per session by the streamable-HTTP transport
//! (see [`crate::mcp::server::router`]) and wires the routing macros from
//! [`rmcp`] to the application-level [`crate::mcp::tools`] module.

use rmcp::{
    ErrorData as McpError, ServerHandler,
    handler::server::{router::tool::ToolRouter, wrapper::Parameters},
    model::*,
    tool, tool_handler, tool_router,
};


use crate::app::AppState;
use crate::mcp::tools;

/// Per-session MCP server handler.
///
/// Holds a clone of the shared [`AppState`] and a [`ToolRouter`] generated by
/// `#[tool_router]`. The router field is read by macro-generated code only,
/// hence the `#[allow(dead_code)]` annotation.
#[derive(Clone)]
pub struct Handler {
    state: AppState,
    #[allow(dead_code)]
    tool_router: ToolRouter<Self>,
}

